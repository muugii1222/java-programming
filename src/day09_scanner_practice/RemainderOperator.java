package day09_scanner_practice;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println(10 / 3);
        System.out.println(10 % 3); // 3+3+3+1 --> 1 is remainder
        System.out.println(10/ 5);
        System.out.println(10 % 5); // 5+5+0 --> no remainder
        System.out.println(20 / 7);
        System.out.println(20.0 / 7); // if you want to exact calculate use decimal numbers!
        System.out.println(20 % 7); // 7+7+6 --> 6 is remainder

    }
}
