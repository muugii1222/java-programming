package day18_conditions_practice_strings_intro;

public class EVenOrOdd {
    public static void main(String[] args) {
        int num = 5;
        if (num % 2 == 0){
            System.out.println("It is even number");
        } else if (num % 2 == 1) {
            System.out.println("It is odd number");
        }



    }
}
