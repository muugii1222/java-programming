package mugi;
/*
Write a program that will create a triangle of asterisks based on size n.

Example:
input: 5
output:
*
**
***
****
*****
 */
public class StarReplIt {
    public static void main(String[] args) {

        for (int i = 80; i >= 20; i--) {
            if (i % 2 == 0) {
                System.out.println(i);
            }

        }

    }
}