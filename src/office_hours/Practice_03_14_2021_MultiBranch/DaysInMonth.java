package office_hours.Practice_03_14_2021_MultiBranch;

/*
Write a program that will accept a number representing the month.
Print out how many days are in that given month. Assume February (2) has 28 days
Ex:
    Input: 1
    Output: 31 days

    Input: 2
    Output: 28 days
 */
public class DaysInMonth {
    public static void main(String[] args) {
        byte month = 2;
        byte days = 28;

        if (month == month || days == days) {
            System.out.println(month + " has " + days + " days");
        }
    }

}
